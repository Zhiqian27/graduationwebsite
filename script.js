// ==============================
// 获取HTML元素
// ==============================

const home = document.getElementById("home");

const messagePage = document.getElementById("message-page");

const studentName = document.getElementById("student-name");

const message = document.getElementById("message");

const player = document.getElementById("player");

const musicBtn = document.getElementById("music-btn");

const backBtn = document.getElementById("back-btn");

const buttons = document.querySelectorAll(".student-btn");

let playing = false;

// ==============================
// 点击名字
// ==============================

buttons.forEach(button => {

    button.addEventListener("click", () => {

        const id = button.dataset.name;

        const data = graduates[id];

        studentName.innerText = "To " + data.name;

        message.innerText = data.message;

        player.src = data.music;

        home.style.display = "none";

        messagePage.style.display = "block";

        player.play();

        playing = true;

        musicBtn.innerText = "⏸ 暂停音乐";

    });

});

// ==============================
// 音乐按钮
// ==============================

musicBtn.addEventListener("click", () => {

    if (playing) {

        player.pause();

        playing = false;

        musicBtn.innerText = "🎵 播放音乐";

    } else {

        player.play();

        playing = true;

        musicBtn.innerText = "⏸ 暂停音乐";

    }

});

// ==============================
// 返回首页
// ==============================

backBtn.addEventListener("click", () => {

    player.pause();

    player.currentTime = 0;

    playing = false;

    musicBtn.innerText = "🎵 播放音乐";

    messagePage.style.display = "none";

    home.style.display = "block";

});